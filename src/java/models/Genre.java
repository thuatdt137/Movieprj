package models;

/**
 *
 * @author thuat
 */
public class Genre {

    private int id;
    private String name;
    private int status;

    public Genre() {
    }

    public Genre(int id, String name, int status) {
        this.id = id;
        this.name = name;
        this.status = status;
    }

    public int getStatus() {
        return status;
    }

    public void setStatus(int status) {
        this.status = status;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    @Override
    public String toString() {
        return "Genre{" + "id=" + id + ", name=" + name + '}';
    }

}
